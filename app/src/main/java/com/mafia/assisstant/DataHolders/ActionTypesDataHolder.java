package com.mafia.assisstant.DataHolders;

public class ActionTypesDataHolder {
    private int id;
    String name;

    public ActionTypesDataHolder(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
